#include <unistd.h>

void	ft_print_comb(void)
{
	int	i;
	int	x;
	int	y;
	int	z;

	i = 0;
	while (i < 1000)
	{
		x = '0' + ((i / 100) % 10);
		y = '0' + ((i / 10) % 10);
		z = '0' + (i % 10);
		if (x < y && y < z)
		{
			write(1, &x, 1);
			write(1, &y, 1);
			write(1, &z, 1);
			if (i < 789)
			{
				write(1, ",", 1);
				write(1, " ", 1);
			}
		}
		i++;
	}
}
