void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *b;
	*b = *a;
	*a = temp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	c;

	c = -1;
	while (++c < size / 2)
		ft_swap(&tab[c], &tab[size - 1 - c]);
}
